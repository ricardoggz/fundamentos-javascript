//Operadores
/*
Nos permiten evaluar ciertas condiciones dentro de nuestro código
*/

//1._==(Doble igualdad): Dos variables tienen el mismo valor, pero no el mismo tipo
let edadNumero = 23 //number
let edadTexto = "23" //cadena de texto

//2._===(triple igualdad): Dos vriables tienen el mismo valor y también el mismo tipo
let edadNumero2 = 23
let edadTexto2 = 23


//3._ AND &&: Permite evaluar dos o mas condiciones que cumplan el mismo valor
//La condicion es que todos cumplan con el mismo valor
let resultado1 = 10
let resultado2 = 20
let resultado3 = 30

//4._OR ||: Permite evaluar diferentes casos con valores que cumplan una condicion en especifica
let resultado4 = 40 //===40
let resultado5 = 50 //60
let resultado6 = 60 //70

//5._ ><: Permite evaluar si un resultado es mayor o menor a otro
//6._ >= <=: Permite evaluar si un resultado es mayor, menor o igual a otro